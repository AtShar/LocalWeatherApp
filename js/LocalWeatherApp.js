$(document).ready(function() {
  //console.log(new Date().getHours()) 
  setbackground();

  $('#degreeButton').click(function() {
    var currentClass = $(this).children("i").attr("class");
    var tempCurrent = $('#tempDegree').html();
    if (currentClass === "wi wi-celsius") {
      var fTemp = tempCurrent * 9 / 5 + 32;
      $('#tempDegree').html(Math.round(fTemp))
      $(this).children("i").attr('class', 'wi wi-fahrenheit');
    } else {
      var cTemp = (tempCurrent - 32) * 5 / 9;
      $('#tempDegree').html(Math.round(cTemp));
      $(this).children("i").attr('class', 'wi wi-celsius');
    }
  });

  if (navigator.geolocation) {
    navigator.geolocation.getCurrentPosition(function(position) {
      var latData = position.coords.latitude;
      var lonData = position.coords.longitude;
      $.getJSON("http://api.openweathermap.org/data/2.5/weather?lat=" + latData + "&lon=" + lonData + "&appid=0b8fbf901847d3f390785a52bd76977e", function(json) {
        $('#cityName').html(json.name + "," + json.sys.country);
        var tempCelcius = parseInt(json.main.temp) - 273;
        $('#tempDegree').html(Math.round(tempCelcius));
        $('#weatherCondition').html(json.weather[0].main);

        $('#pressureContent').html(json.main.pressure);
        $('#humidityContent').html(json.main.humidity);
        $('#windContent').html(json.wind.speed);
        $('#visiblityContent').html(json.visibility);
        $('#minTempContent').html(parseInt(json.main.temp_min) - 273);
        $('#maxTempContent').html(parseInt(json.main.temp_max) - 273);
        var sunrise = new Date(json.sys.sunrise * 1000);
        var sunset = new Date(json.sys.sunrise * 1000);
        $('#sunriseContent').html(getTimeFromUTC(sunrise));
        $('#sunsetContent').html(getTimeFromUTC(sunset));
        /* $('#weatherImage').attr('src',"http://openweathermap.org/img/w/"+json.weather[0].icon+".png"); */
        /* $('#weatherIcon').attr('alt',json.weather[0].main); */

        var prefix = 'wi wi-';
        var code = json.weather[0].id;
        var icon = weatherIcons[code].icon;

        // If we are not in the ranges mentioned above, add a day/night prefix.
        if (!(code > 699 && code < 800) && !(code > 899 && code < 1000)) {
          icon = 'day-' + icon;
        }

        // Finally tack on the prefix.
        icon = prefix + icon;
        console.log(icon);
        $('#weatherIcon').addClass(icon);
      });
    });
  }

});

function getTimeFromUTC(date) {
  var hours = date.getHours();
  // Minutes part from the timestamp
  var minutes = "0" + date.getMinutes();
  // Seconds part from the timestamp
  var seconds = "0" + date.getSeconds();

  // Will display time in 10:30:23 format
  var formattedTime = hours + ':' + minutes.substr(-2) + ':' + seconds.substr(-2);

  return formattedTime;
}

function setbackground() {
  var index = new Date().getHours();
  if (index > 5 && index <= 7)
    ColorValue = "clearMorning";
  else if (index > 7 && index <= 17)
    ColorValue = "clearDay";
  else if (index > 17 && index <= 19)
    ColorValue = "clearMorning";
  else if (index > 19 && index <= 23)
    ColorValue = "clearNight";
  else if (index >= 0 && index <= 5)
    ColorValue = "clearNight";

  $("#mainContainer").addClass(ColorValue);
  $("#degreeButton").addClass(ColorValue);

}